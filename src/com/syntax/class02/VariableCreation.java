		num1=100; //assign value or initialize variable 
		num2=200;
		num3=300;
		
		//Change value of variable 
		num2=2000000; //reassing the value java takes the latest 
		
		System.out.println(num2); 
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	
		
