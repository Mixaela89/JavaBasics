		byte b=10; // create a variable and assign value
		
		//2 way to create a variable
		int i; //create a variable
		i=100; //assign value later
		// we need to create a variable only once then we can re use it multiple times
		
		// 3 way to create a variable useful when we need to use the same type data values 
