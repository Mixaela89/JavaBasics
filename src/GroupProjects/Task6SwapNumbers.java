package GroupProjects;

public class Task6SwapNumbers {

	public static void main(String[] args) {
		// Write a program to swap 2 numbers without a temporary variable?
		
		int a=10; 
		int b=20;
		
		a=a+b; //a=30
		b=a-b; //30-20=10
		
		a=a-b; 
		
		
		System.out.println(a);
		System.out.println(b);
		
	}


}
 
              